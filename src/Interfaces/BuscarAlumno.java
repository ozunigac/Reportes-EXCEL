    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        matriculaTxt.getText();
        int matricula = 0;
        Alumno alumno=null;
        if(matriculaTxt.getText().isEmpty()){
            errorTxt.setText("Favor de ingresar una matricula");
        }
        Archivo archivo = new Archivo();
        if(archivo.isNumeric(matriculaTxt.getText())){
            errorTxt.setText("Matricula invalida");
        }else{
            matricula=Integer.parseInt(matriculaTxt.getText());
            alumno= archivo.buscarAlumnoMatricula(matricula);
            if(alumno == null){
                errorTxt.setText("no se encontr√≥ el alumno");
            }else{
                infoTxt.append(alumno.getNombre()+" "+alumno.getApellidoPa()+" "+alumno.getApellidoMa()+"\n");
                for(int i=0;i<alumno.getMaterias().size();i++){
                    infoTxt.append("Materia: "+alumno.getMaterias().get(i).getNombre()+"\n");
                infoTxt.append("Asistencia: "+alumno.getMaterias().get(i).getAsistencia()+"\n");
                infoTxt.append("U1: "+alumno.getMaterias().get(i).getU1()+" \t U2: "+alumno.getMaterias().get(i).getU2()+" \t U3: "+
                        alumno.getMaterias().get(i).getU3()+" \t U4: "+alumno.getMaterias().get(i).getU4()+"\n");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    

