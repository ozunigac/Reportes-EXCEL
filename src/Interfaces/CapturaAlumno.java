    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Archivo ar = new Archivo();
        int matricula=0;
        int asistencia=0;
        float u1=0,u2=0,u3=0,u4=0;
        boolean datosCorrectos=false;
        /*********************************************************************************************
        ******************esta seccion validar치 que los campos no esten vacios************************
        *********************************************************************************************/
        if(nombreTxt.getText().isEmpty()||APaternoTxt.getText().isEmpty()||AMaternoTxt.getText().isEmpty()
                ||matriculaTxt.getText().isEmpty()||matriculaTxt.getText().isEmpty()||materiaTxt.getText().isEmpty()
                ||codMateriaTxt.getText().isEmpty()||asistenciaTxt.getText().isEmpty()||u1Txt.getText().isEmpty()
                ||u2Txt.getText().isEmpty()||u3Txt.getText().isEmpty()||u4Txt.getText().isEmpty()){
            errorTxt.setText("Favor de no dejar espacios vacios");
        }else{
            
            /***************************************************************************************+++
             ******************* esta seccion validar치 que los datos sean numericos********************+++
             *********************************************************************************************/
            if(ar.isNumeric(matriculaTxt.getText())){
                matricula = Integer.parseInt(matriculaTxt.getText());
                if(ar.isNumeric(asistenciaTxt.getText())){
                    asistencia = Integer.parseInt(asistenciaTxt.getText());
                    if(ar.isNumeric(u1Txt.getText())){
                        u1 = Integer.parseInt(u1Txt.getText());
                        if(ar.isNumeric(u2Txt.getText())){
                            u2 = Integer.parseInt(u2Txt.getText());
                            if(ar.isNumeric(u3Txt.getText())){
                                u3 = Integer.parseInt(u3Txt.getText());
                                if(ar.isNumeric(u4Txt.getText())){
                                    u4 = Integer.parseInt(u4Txt.getText());
                                    if(ar.isValid(u1)){
                                        if(ar.isValid(u2)){
                                            if(ar.isValid(u3)){
                                                if(ar.isValid(u4)){
                                                    if(ar.isValid(asistencia)){
                                                        datosCorrectos=true;   
                                                    }else
                                                        errorTxt.setText("Ingrese valor del 0 al 100 en asistencia");
                                                }else
                                                    errorTxt.setText("Ingrese un valor del 0 al 100 en la U4");
                                            }else
                                                errorTxt.setText("Ingrese un valor del 0 al 100 en la U3");
                                        }else
                                            errorTxt.setText("Ingrese un valor del 0 al 100 en la U2");
                                    }else
                                        errorTxt.setText("Ingrese un valor del 0 al 100 en la U1");
                                }
                                else
                                    errorTxt.setText("Unidad 4 incorrecta ingrese datos numericos");
                            }else
                                errorTxt.setText("Unidad 3 incorrecta ingrese datos numericos");
                        }else
                           errorTxt.setText("Unidad 2 incorrecta ingrese datos numericos"); 
                    }else
                        errorTxt.setText("Unidad 1 incorrecta ingrese datos numericos");
                }else
                    errorTxt.setText("Asistencia incorrecta ingrese datos numericos");
            }else
                errorTxt.setText("Matricula incorrecta ingrese datos numericos");
        
        }
        //se agregaran los valores de las cajas de texto a las variables
        String nombre = nombreTxt.getText();
        String APaterno = APaternoTxt.getText();
        String AMaterno = AMaternoTxt.getText();
        String nombreMateria = materiaTxt.getText();
        String codigoMateria = codMateriaTxt.getText();
        
        //validar치 que el alumno no sobrepase el limite de materias
        if(ar.contarMaterias(matricula)>=8&&datosCorrectos==true){
                errorTxt.setText("El alumno ya cuenta con 8 materias registradas");
                datosCorrectos=false;
        }
        
        //se contar치 el numero maximo de alumnos en una materia
        if(ar.contarAlumnosMateria(codigoMateria)>=32&&datosCorrectos==true){
            errorTxt.setText("La materia ya tiene el limite de alumnos registrados");
            datosCorrectos=false;
        }
        
        
        //si todos los datos son correctos y se hicieron todas las validaciones entonces se hara el registro
        if(datosCorrectos==true){
            //se limpiara el campo de texto que arroja los errores
            errorTxt.setText("");

            ArrayList<Materia> materia = new ArrayList<Materia>();
            materia.add(new Materia(nombreMateria,codigoMateria,asistencia,u1,u2,u3,u4,(u1+u2+u3+u4)/4));
            Alumno alumno = new Alumno(nombre,APaterno,AMaterno,matricula,materia);

            try {
                ar.insertarAlumno(alumno);
                errorTxt.setText("Registro exitoso");
            } catch (IOException ex) {
                errorTxt.setText("no se pudo hacer el registro");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

